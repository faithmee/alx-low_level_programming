/**
 *main - Check the code
 *description - Write a function that checks for a digit (0 through 9).
 *_isdigit - Check if numbers are 0 - 9
 *@c: Char to b checked
 *
 *Return: 0 or 1
 */
int main(void)
{
	int _isdigit(int c);
	{
		if (c >= '0' && c <= '9')
			return (1);
		else 
			return (0);
	}
}
